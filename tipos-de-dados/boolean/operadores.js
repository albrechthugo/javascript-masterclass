// operadores de comparacao

console.log(0 == "0"); // compara apenas o valor
console.log(0 === "0"); // compara valor e tipo
console.log(10 == 20);
console.log("a" != "b");
console.log(6 > 4);
console.log(5 <= 5);
console.log(false == undefined);
console.log(false == null);
console.log(undefined == null);

// operadores logicos

console.log(0 || 2);
console.log(1 || 2);
console.log(1 && 2);
console.log(0 && 2);

// operador ternario

console.log(10 ? "good" : "bad");
console.log(0 ? "good" : "bad");

let grade = 10 ? "good" : "bad";
console.log(grade);
