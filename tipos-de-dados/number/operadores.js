console.log("\natribuicao \n");

let result = 10;
result += 2; // result = result + 2
console.log(result);

result -= 5; // result = result - 5
console.log(result);

result *= 8; // result = result * 8
console.log(result);

result /= 2; // result = result / 2
console.log(result);

result %= 8; // result = result % 8
console.log(result);

console.log("\nincremento e decremento \n");

result = 20;

result++; // retorna e incrementa
++result; // incrementa e retorna
result--; // retorna e decrementa
--result; // decrementa e retorna

console.log("\nbinarios \n");

console.log(4 | 3); // so eh 0 quando for 0 e 0, resto eh 1
console.log(4 & 3); // so eh 1 quando for 1 e 1, resto eh 0
console.log(4 ^ 3); // so eh 1 quando for 1 e 0, resto eh 0
console.log(~4); // inverte todos os bits
console.log(4 << 2);
console.log(128 >> 1);
console.log(-2 >>> 1);
